    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (jTextField2.getText().equals("") || jTextField1.getText().equals("")  || new String(jPasswordField1.getPassword()).equals("")){
            JOptionPane.showMessageDialog(null, "Preecncha todos os campos");
        }else{
            String nome = jTextField2.getText();
            String mat = jTextField1.getText();
            String senha = jPasswordField1.getText();
            
            Cadastro cad = new Cadastro();
            cad.CadastrarFuncionario(mat, nome, senha);
            jTextField2.setText("");
            jTextField1.setText("");
            jPasswordField1.setText("");
            consultafun();
        
         }
    }//GEN-LAST:event_jButton1ActionPerformed

