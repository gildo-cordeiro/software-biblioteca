    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if(titulo.getText().equals("") && genero.getText().equals("") && autor.getText().equals("") && editora.getText().equals("")){
                consulta();
            }else{
                int inidice = comboBox.getSelectedIndex();

                Modelo.setNumRows(0);
                Consulta atr = new Consulta();        
                for (livro livro: atr.ConsultarLivro(titulo.getText(),editora.getText(),genero.getText(), autor.getText(), inidice)) {
                     Modelo.addRow(new Object[]{livro.getNome(),livro.getEditora(),livro.getGenero(),livro.getAutor()});
                }
                atr.FecharConexao();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

