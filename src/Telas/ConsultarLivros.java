    public void consulta(){
        Modelo.setNumRows(0);
        Consulta produtos = new Consulta();        
        for (livro atr: produtos.ConsultarLivro()) {
            Modelo.addRow(new Object[]{atr.getNome(),atr.getEditora(),atr.getGenero(),atr.getAutor()});
        }
        produtos.FecharConexao();
    }
    
    public void ChangeIndice(){
       int num = comboBox.getSelectedIndex();
       switch (num) {
           case 0:
               titulo.setVisible(true);
               genero.setVisible(false);
               autor.setVisible(false);
               editora.setVisible(false);
               genero.setText("");
               autor.setText("");
               editora.setText("");
               consulta();
               break;
           case 1:
               editora.setVisible(true);
               genero.setVisible(false);
               autor.setVisible(false);
               titulo.setVisible(false);
               genero.setText("");
               autor.setText("");
               titulo.setText("");
               consulta();
               break;
           case 2:
               genero.setVisible(true);
               editora.setVisible(false);
               autor.setVisible(false);
               titulo.setVisible(false);
               editora.setText("");
               autor.setText("");
               titulo.setText("");
               consulta();
               break;
           case 3:
               autor.setVisible(true);
               editora.setVisible(false);
               genero.setVisible(false);
               titulo.setVisible(false);
               titulo.setText("");
               genero.setText("");
               editora.setText("");
               consulta();
               break;
           default:
               break;
       }
    }

