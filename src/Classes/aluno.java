package Classes;

public class aluno {
    private String mat;
    private String nome;
    private String turma;
    private String senha;
    private String email;
    private String punicao;
    private int quant_livros;
    private int quant_punicao;

    public int getQuant_punicao() {
        return quant_punicao;
    }

    public void setQuant_punicao(int quant_punicao) {
        this.quant_punicao = quant_punicao;
    }

    public int getQuant_livros() {
        return quant_livros;
    }

    public void setQuant_livros(int quant_livros) {
        this.quant_livros = quant_livros;
    }

    public String getPunicao() {
        return punicao;
    }

    public void setPunicao(String punicao) {
        this.punicao = punicao;
    }

    public String getMat() {
        return mat;
    }

    public void setMat(String mat) {
        this.mat = mat;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTurma() {
        return turma;
    }

    public void setTurma(String turma) {
        this.turma = turma;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}
